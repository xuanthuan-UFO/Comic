package com.xuanthuan.comic.object;

public class Object_timkiem {
    String ten_search, hinh_search;

    public Object_timkiem(String ten_search, String hinh_search) {
        this.ten_search = ten_search;
        this.hinh_search = hinh_search;
    }

    public String getTen_search() {
        return ten_search;
    }

    public void setTen_search(String ten_search) {
        this.ten_search = ten_search;
    }

    public String getHinh_search() {
        return hinh_search;
    }

    public void setHinh_search(String hinh_search) {
        this.hinh_search = hinh_search;
    }
}
